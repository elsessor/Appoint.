export async function unfriend(req, res) {
  try {
    const myId = req.user.id;
    const { id: friendId } = req.params;

    if (myId === friendId) {
      return res.status(400).json({ message: "You can't unfriend yourself" });
    }

    const friend = await User.findById(friendId);
    if (!friend) {
      return res.status(404).json({ message: "Friend not found" });
    }

    await User.findByIdAndUpdate(myId, {
      $pull: { friends: friendId },
    });

    await User.findByIdAndUpdate(friendId, {
      $pull: { friends: myId },
    });

    res.status(200).json({ message: "Friend removed successfully" });
  } catch (error) {
    console.error("Error in unfriend controller", error.message);
    res.status(500).json({ message: "Internal Server Error" });
  }
}

export async function markNotificationsRead(req, res) {
  try {
    const userId = req.user.id;

    await FriendRequest.updateMany(
      { recipient: userId, status: "pending", recipientSeen: false },
      { recipientSeen: true }
    );

    await FriendRequest.updateMany(
      { sender: userId, status: "accepted", senderSeen: false },
      { senderSeen: true }
    );

    res.status(200).json({ message: "Notifications marked as read" });
  } catch (error) {
    console.log("Error in markNotificationsRead controller", error.message);
    res.status(500).json({ message: "Internal Server Error" });
  }
}